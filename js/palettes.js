const mkC = (pal, dark) => {
  const primary = dark ? pal.dkPrimary : pal.primary;
  const second  = dark ? pal.dkSecond  : pal.second;
  return {
    bg:        dark ? pal.dkBg   : pal.bg,
    surf:      dark ? pal.dkSurf : pal.surf,
    text:      dark ? pal.dkText : pal.text,
    primary,
    second,
    cardDk:    dark ? pal.dkSurf : pal.text,
    cardDkTxt: dark ? pal.dkText : pal.bg,
    icon:      dark ? primary    : second,
    border:    dark ? 'rgba(255,255,255,0.07)' : pal.surf,
    victory:   dark ? pal.vD     : pal.vL,
    defeat:    dark ? pal.dD     : pal.dL,
  };
};
